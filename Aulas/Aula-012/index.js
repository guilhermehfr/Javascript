let num1 = 5.52;

// Arredonda o número para baixo
console.log(Math.floor(num1));

// Ceil arredonda o número para cima
console.log(Math.ceil(num1));

// Round, arredonda o número em geral. ( 1.55 = 2 / 1.49 = 1 )
console.log(Math.round(num1));

// Mostra o valor de PI
console.log(Math.PI);

// Os metodos max e min, procuram respectivamente os maiores, e menores números de uma lista de valores.
console.log(Math.max(0, 5, 12, 3, 20, 1, -540, -12, 2, 954, -10));
console.log(Math.min(0, 5, 12, 3, 20, 1, -540, -12, 2, 954, -10));

